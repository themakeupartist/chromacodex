import { mkdir, readFile, writeFile } from "node:fs/promises";
import { dirname, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const defaultNormalizedPath = resolve(process.cwd(), "src/data/generated/workbook-normalized.json");
const defaultSeedPath = resolve(process.cwd(), "supabase/seed.sql");

function sqlString(value) {
  return `'${String(value).replaceAll("'", "''")}'`;
}

function sqlArray(values) {
  if (!values || values.length === 0) {
    return "ARRAY[]::text[]";
  }

  return `ARRAY[${values.map((value) => sqlString(value)).join(", ")}]`;
}

function sqlValue(value) {
  if (value == null) {
    return "null";
  }

  if (Array.isArray(value)) {
    return sqlArray(value);
  }

  if (typeof value === "number") {
    return Number.isFinite(value) ? String(value) : "null";
  }

  if (typeof value === "boolean") {
    return value ? "true" : "false";
  }

  return sqlString(value);
}

function paintCompletenessScore(paint) {
  return (paint.hasMeasurement ? 4 : 0) + (paint.hasBehavior ? 2 : 0) + (paint.pigmentCount > 0 ? 1 : 0);
}

function comparePaints(left, right) {
  const scoreDifference = paintCompletenessScore(right) - paintCompletenessScore(left);

  if (scoreDifference !== 0) {
    return scoreDifference;
  }

  const recordDifference = (left.recordId ?? Number.MAX_SAFE_INTEGER) - (right.recordId ?? Number.MAX_SAFE_INTEGER);

  if (recordDifference !== 0) {
    return recordDifference;
  }

  return left.id.localeCompare(right.id);
}

function chooseSeedablePaints(normalized) {
  const paintById = new Map(normalized.entities.paints.map((paint) => [paint.id, paint]));
  const skippedPaintIds = new Set();
  const duplicateResolution = [];

  normalized.integrity.duplicateCanonicalKeys.forEach((group) => {
    const contenders = group.ids.map((id) => paintById.get(id)).filter(Boolean).sort(comparePaints);

    if (contenders.length === 0) {
      return;
    }

    const kept = contenders[0];
    const skipped = contenders.slice(1).map((paint) => paint.id);

    skipped.forEach((id) => skippedPaintIds.add(id));

    duplicateResolution.push({
      canonicalKey: group.key,
      keptPaintId: kept.id,
      skippedPaintIds: skipped
    });
  });

  return {
    duplicateResolution,
    skippedPaintIds,
    paints: normalized.entities.paints.filter((paint) => !skippedPaintIds.has(paint.id))
  };
}

function assertSeedable(normalized, paints) {
  const missingSheetCount = normalized.integrity.missingSheets.length;

  if (missingSheetCount > 0) {
    throw new Error(`Cannot generate seed SQL because ${missingSheetCount} workbook sheets are missing.`);
  }

  const missingReferenceCount = Object.values(normalized.integrity.missingReferences).reduce((sum, entries) => sum + entries.length, 0);

  if (missingReferenceCount > 0) {
    throw new Error(`Cannot generate seed SQL because ${missingReferenceCount} missing foreign-key references were detected.`);
  }

  const canonicalKeys = new Set();

  paints.forEach((paint) => {
    if (!paint.canonicalKey) {
      throw new Error(`Cannot generate seed SQL because paint ${paint.id} is missing a canonical key.`);
    }

    if (canonicalKeys.has(paint.canonicalKey)) {
      throw new Error(`Cannot generate seed SQL because canonical key ${paint.canonicalKey} is still duplicated after filtering.`);
    }

    canonicalKeys.add(paint.canonicalKey);
  });
}

function renderInsert(tableName, columns, rows) {
  if (rows.length === 0) {
    return `-- No rows to insert into ${tableName}.\n`;
  }

  const tuples = rows.map((row) => `  (${row.map((value) => sqlValue(value)).join(", ")})`).join(",\n");

  return `insert into ${tableName} (${columns.join(", ")})\nvalues\n${tuples};\n`;
}

export function buildSeedSql(normalized) {
  const seedable = chooseSeedablePaints(normalized);
  assertSeedable(normalized, seedable.paints);

  const seedablePaintIds = new Set(seedable.paints.map((paint) => paint.id));
  const paintPigments = normalized.entities.paintPigments.filter((entry) => seedablePaintIds.has(entry.paintId));
  const paintBehavior = normalized.entities.paintBehavior.filter((entry) => seedablePaintIds.has(entry.paintId));
  const measurements = normalized.entities.measurements.filter((entry) => seedablePaintIds.has(entry.paintId));

  const lines = [
    "-- Generated by scripts/generate-seed-sql.mjs from src/data/generated/workbook-normalized.json.",
    `-- Workbook: ${normalized.workbook}`,
    `-- Generated at: ${normalized.generatedAt}`,
    `-- Imported source rows: ${normalized.rowCounts.paints} paints, ${normalized.rowCounts.pigments} pigments, ${normalized.rowCounts.paintPigments} paint-pigment links.`,
    `-- Seeded rows: ${seedable.paints.length} paints, ${paintBehavior.length} behavior rows, ${measurements.length} measurement rows.`,
    `-- Duplicate canonical keys skipped during seed generation: ${seedable.skippedPaintIds.size}.`,
    `-- Suspicious pigment codes still present for review: ${normalized.integrity.suspiciousPigmentCodes.length}.`,
    "",
    "begin;",
    "",
    "truncate table",
    "  public.measurements,",
    "  public.paint_behavior,",
    "  public.paint_pigments,",
    "  public.paints,",
    "  public.pigments,",
    "  public.product_lines,",
    "  public.mediums,",
    "  public.brands,",
    "  public.sources",
    "restart identity cascade;",
    ""
  ];

  if (seedable.duplicateResolution.length > 0) {
    lines.push("-- Duplicate canonical-key groups resolved during seed generation:");
    seedable.duplicateResolution.forEach((entry) => {
      lines.push(`-- kept ${entry.keptPaintId} for ${entry.canonicalKey}; skipped ${entry.skippedPaintIds.join(", ")}.`);
    });
    lines.push("");
  }

  lines.push(
    renderInsert(
      "public.sources",
      ["id", "name", "source_type", "source_url", "reliability_role", "notes"],
      normalized.entities.sources.map((entry) => [entry.id, entry.name, entry.type, entry.url, entry.reliabilityRole, entry.notes])
    ).trimEnd(),
    "",
    renderInsert(
      "public.brands",
      ["id", "name", "slug", "manufacturer", "synonyms", "notes"],
      normalized.entities.brands.map((entry) => [entry.id, entry.name, entry.slug, entry.manufacturer, entry.synonyms, entry.notes])
    ).trimEnd(),
    "",
    renderInsert(
      "public.mediums",
      ["id", "name", "slug", "parent_category", "core_behavior_notes"],
      normalized.entities.mediums.map((entry) => [entry.id, entry.name, entry.slug, entry.parentCategory, entry.coreBehaviorNotes])
    ).trimEnd(),
    "",
    renderInsert(
      "public.product_lines",
      ["id", "brand_id", "medium_id", "name", "slug", "formulation_family", "product_line_type", "viscosity", "finish", "open_time_profile", "notes"],
      normalized.entities.productLines.map((entry) => [
        entry.id,
        entry.brandId,
        entry.mediumId,
        entry.name,
        entry.slug,
        entry.formulationFamily,
        entry.productLineType,
        entry.viscosity,
        entry.finish,
        entry.openTimeProfile,
        entry.notes
      ])
    ).trimEnd(),
    "",
    renderInsert(
      "public.pigments",
      ["id", "code", "slug", "pigment_full_name", "ci_name", "organic_or_inorganic", "pigment_origin", "color_family", "status", "notes"],
      normalized.entities.pigments.map((entry) => [
        entry.id,
        entry.code,
        entry.slug,
        entry.fullName,
        entry.ciName,
        entry.organicOrInorganic,
        entry.origin,
        entry.colorFamily,
        entry.status,
        entry.notes
      ])
    ).trimEnd(),
    "",
    renderInsert(
      "public.paints",
      ["id", "brand_id", "medium_id", "product_line_id", "source_id", "name", "slug", "canonical_key", "color_family", "pigment_count", "single_pigment", "verification_status", "notes"],
      seedable.paints.map((entry) => [
        entry.id,
        entry.brandId,
        entry.mediumId,
        entry.productLineId,
        entry.sourceId,
        entry.name,
        entry.slug,
        entry.canonicalKey,
        entry.colorFamily,
        entry.pigmentCount,
        entry.singlePigment,
        entry.verificationStatus,
        entry.notes
      ])
    ).trimEnd(),
    "",
    renderInsert(
      "public.paint_pigments",
      ["paint_id", "pigment_id", "pigment_code", "pigment_sequence", "relationship_type", "verification_status", "notes"],
      paintPigments.map((entry) => [
        entry.paintId,
        entry.pigmentId,
        entry.pigmentCode,
        entry.pigmentSequence,
        entry.relationshipType,
        entry.verificationStatus,
        entry.notes
      ])
    ).trimEnd(),
    "",
    renderInsert(
      "public.paint_behavior",
      ["paint_id", "transparency", "opacity_code", "permanence", "granulation", "staining", "temperature", "finish", "viscosity", "open_time_profile", "behavior_source", "verification_status", "notes"],
      paintBehavior.map((entry) => [
        entry.paintId,
        entry.transparency,
        entry.opacityCode,
        entry.permanence,
        entry.granulation,
        entry.staining,
        entry.temperature,
        entry.finish,
        entry.viscosity,
        entry.openTimeProfile,
        entry.behaviorSource,
        entry.verificationStatus,
        entry.notes
      ])
    ).trimEnd(),
    "",
    renderInsert(
      "public.measurements",
      ["paint_id", "source_id", "measurement_type", "density", "rgb_red", "rgb_green", "rgb_blue", "rgb_combined", "swatch", "measurement_method", "source_url", "notes"],
      measurements.map((entry) => [
        entry.paintId,
        entry.sourceId,
        entry.measurementType,
        entry.density,
        entry.rgb?.red ?? null,
        entry.rgb?.green ?? null,
        entry.rgb?.blue ?? null,
        entry.rgbCombined,
        entry.swatch,
        entry.measurementMethod,
        entry.sourceUrl,
        entry.notes
      ])
    ).trimEnd(),
    "",
    "commit;"
  );

  return lines.join("\n");
}

export async function writeSeedSql(normalized, outputPath = defaultSeedPath) {
  const sql = buildSeedSql(normalized);

  await mkdir(dirname(outputPath), { recursive: true });
  await writeFile(outputPath, sql);

  return outputPath;
}

async function main() {
  const normalized = JSON.parse(await readFile(defaultNormalizedPath, "utf8"));
  const outputPath = await writeSeedSql(normalized, defaultSeedPath);

  console.log(`Wrote Supabase seed SQL to ${outputPath}`);
}

if (process.argv[1] && fileURLToPath(import.meta.url) === process.argv[1]) {
  main().catch((error) => {
    console.error(error);
    process.exitCode = 1;
  });
}
